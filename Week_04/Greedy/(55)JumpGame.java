class Solution {
    public boolean canJump(int[] nums) {
        // go backward to find a good position that can reach last position
        int lastPos = nums.length - 1;
        for (int i = nums.length - 1; i >= 0; i--) {
            if (i + nums[i] >= lastPos) {
                lastPos = i;
            }
        }
        return lastPos == 0;
    }
}